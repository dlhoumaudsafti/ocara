/// Mapping mot-clé

use crate::parsing::token::TokenKind;

pub(in crate::parsing::lexer) fn keyword_or_ident(s: &str) -> TokenKind {
    match s {
        "import"     => TokenKind::Import,
        "from"       => TokenKind::From,
        "namespace"  => TokenKind::Namespace,
        "as"         => TokenKind::As,
        "var"        => TokenKind::Var,
        "scoped"     => TokenKind::Scoped,
        "property"   => TokenKind::Property,
        "const"      => TokenKind::Const,
        "function"   => TokenKind::Function,
        "method"     => TokenKind::Method,
        "class"      => TokenKind::Class,
        "generic"    => TokenKind::Generic,
        "module"     => TokenKind::Module,
        "enum"       => TokenKind::Enum,
        "interface"  => TokenKind::Interface,
        "extends"    => TokenKind::Extends,
        "modules"    => TokenKind::Modules,
        "implements" => TokenKind::Implements,
        "init"       => TokenKind::Init,
        "public"     => TokenKind::Public,
        "private"    => TokenKind::Private,
        "protected"  => TokenKind::Protected,
        "static"     => TokenKind::Static,
        "if"         => TokenKind::If,
        "elseif"     => TokenKind::Elseif,
        "else"       => TokenKind::Else,
        "switch"     => TokenKind::Switch,
        "default"    => TokenKind::Default,
        "match"      => TokenKind::Match,
        "while"      => TokenKind::While,
        "for"        => TokenKind::For,
        "in"         => TokenKind::In,
        "return"     => TokenKind::Return,
        "use"        => TokenKind::Use,
        "break"      => TokenKind::Break,
        "continue"   => TokenKind::Continue,
        "try"        => TokenKind::Try,
        "on"         => TokenKind::On,
        "is"         => TokenKind::Is,
        "raise"      => TokenKind::Raise,
        "self"       => TokenKind::SelfKw,
        "async"      => TokenKind::Async,
        "resolve"    => TokenKind::Resolve,
        "variadic"   => TokenKind::Variadic,
        "runtime"    => TokenKind::Runtime,
        "main"       => TokenKind::Main,
        "error"      => TokenKind::Error,
        "success"    => TokenKind::Success,
        "exit"       => TokenKind::Exit,
        "int"        => TokenKind::TInt,
        "float"      => TokenKind::TFloat,
        "string"     => TokenKind::TString,
        "bool"       => TokenKind::TBool,
        "mixed"      => TokenKind::TMixed,
        "map"        => TokenKind::TMap,
        "void"       => TokenKind::TVoid,
        "true"       => TokenKind::LitTrue,
        "false"      => TokenKind::LitFalse,
        "null"       => TokenKind::LitNull,
        "and"        => TokenKind::KwAnd,
        "or"         => TokenKind::KwOr,
        "not"        => TokenKind::KwNot,
        "egal"       => TokenKind::KwEgal,
        "nameless"   => TokenKind::KwNameless,
        _            => TokenKind::Ident(s.to_string()),
    }
}
